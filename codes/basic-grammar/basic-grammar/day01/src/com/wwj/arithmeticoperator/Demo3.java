package com.wwj.arithmeticoperator;

public class Demo3 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++); // 10
        a = 10;
        System.out.println(++a); // 11
        a = 10;
        System.out.println(a--); // 10
        a = 10;
        System.out.println(--a); // 9
    }
}
